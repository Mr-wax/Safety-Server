export const getAllAlerts = async (req, res) => {
  try {
    const alerts = await Alert.find().populate('user', 'firstname lastname email');
    res.status(200).json(alerts);
  } catch (error) {
    res.status(500).json({ error: 'Server error' });
  }
};
